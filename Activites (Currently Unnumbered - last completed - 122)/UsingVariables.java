public class UsingVariables
{
	public static void main( String[] args )
	{

		int room;
		double eValue;
		String topic;

		room = 113;
		eValue = 2.71828;
		topic = "Computer Science";

		System.out.println( "This is room # " + room );
		System.out.println( "e is close to " + eValue );
		System.out.println( "I am learning a bit about " + topic );

	}
}